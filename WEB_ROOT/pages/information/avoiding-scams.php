            <h2>Avoiding Scams</h2>
        </div>

        <p><strong>Stay safe when buying or selling online.</strong> Craigslist is all about local, person‑to‑person deals. If an offer sounds too good to be true, it probably is. Here are a few simple rules to help you avoid common scams.</p>

        <h4 style="margin-top:1rem; color:#800080;">🔒 General Safety Tips</h4>
        <ul style="margin-left:1.5rem; margin-bottom:1rem;">
            <li><strong>Deal locally, face‑to‑face</strong> – meet in a public place (café, bank, police station lobby).</li>
            <li><strong>Never send money</strong> – revolut.me, Paypal, or any other wire service. Scammers often insist on this.</li>
            <li><strong>Cash is king</strong> – For smaller items, use cash. For larger amounts, consider a  check or send after reciept, but only after meeting the seller.</li>
            <li><strong>Don’t share personal info</strong> – never give out your bank details, social insurance number, or copies of ID.</li>
            <li><strong>Check the item before paying</strong> – test electronics, inspect vehicles, and read all documents.</li>
            <li><strong>Trust your instincts</strong> – if something feels off, walk away.</li>
        </ul>

        <h4 style="margin-top:1rem; color:#800080;">⚠️ Common Scams to Watch For</h4>
        <ul style="margin-left:1.5rem; margin-bottom:1rem;">
            <li><strong>Fake payment services</strong> – buyer sends a fake PayPal email saying money is “on hold” until you ship.</li>
            <li><strong>Overpayment / cashier’s check scam</strong> – buyer sends a cheque for too much, asks you to wire the difference. The cheque later bounces.</li>
            <li><strong>“Guarantee” or “escrow” sites</strong> – scammer wants to use a fake third‑party site. Real escrow is rarely used for classifieds.</li>
            <li><strong>Rental scams</strong> – someone “out of the country” wants a deposit before you’ve seen the flat. Always view the property first.</li>
            <li><strong>Fake classifieds</strong> – items priced far below market value to lure you into wiring money.</li>
        </ul>

        <h4 style="margin-top:1rem; color:#800080;">📞 If You’ve Been Scammed</h4>
        <p>Stop all communication. Contact your bank immediately if you shared financial details. Report the incident to <a href="/?page=in_development&undevelopedname=Info_ActionFraud">Action Fraud</a> (UK) and to Craigslist by using the “flag” link on the posting. You can also forward scam emails to <a href="mailto:spam@craigslist.org">spam@craigslist.org</a>.</p>

        <p style="margin-top:1rem; padding:0.5rem; background:#f0edf4; border-left:4px solid #800080;">
            <strong>Remember:</strong> Craigslist does not get involved in transactions, does not offer buyer protection, and never guarantees deals. Keep everything local and in person.
        </p>
